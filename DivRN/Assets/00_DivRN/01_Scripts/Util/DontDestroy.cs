	このスクリプトがアサインされているオブジェクトは
	[ Application.LoadLevel ] による現状シーン破棄処理が実行されても破棄されない。
	
	シーンをまたいで存在すべきオブジェクトを想定。
	シーンの読み込みが完了した際に、SceneManagerにより全てのオブジェクトに対して [ SendDontDestroyON ] が発行される
*/
