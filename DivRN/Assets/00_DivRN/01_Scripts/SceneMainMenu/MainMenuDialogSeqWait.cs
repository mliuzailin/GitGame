/*!
	@brief	メインメニューダイアログUIシーケンス：待つだけ
*/
//----------------------------------------------------------------------------
public class MainMenuDialogSeqWait : LayoutSwitchSeq
{
    /*==========================================================================*/
    /*		var																	*/
    /*==========================================================================*/
    /*==========================================================================*/
    /*		func																*/
    /*==========================================================================*/
    //----------------------------------------------------------------------------
    /*!
		@brief	Unity固有処理：初期化処理	※初回のUpdateを呼び出す直前に呼出し
	*/
    //----------------------------------------------------------------------------
    protected override void Start()
    {
        base.Start();
    }

    //----------------------------------------------------------------------------
    /*!
		@brief	Unity固有処理：更新処理	※定期処理
	*/
    //----------------------------------------------------------------------------
    public void Update()
    {
        //----------------------------------------
        // 固定処理
        // 管理側からの更新許可やフェード待ち含む。
        //----------------------------------------
        if (LayoutSwitchUpdate() == false)
        {
            return;
        }
    }

    //----------------------------------------------------------------------------
    /*!
		@brief	基底継承：TitleMenuSeq：ページ切り替えにより有効化された際に呼ばれる関数
		@note	ページのレイアウト再構築を兼ねる
	*/
    //----------------------------------------------------------------------------
    protected override void LayoutSwitchSetting(bool bActive)
    {
        //--------------------------------
        // 規定呼び出し
        //--------------------------------
        base.LayoutSwitchSetting(bActive);

        //--------------------------------
        // 以下は有効になったタイミングでの処理なので、
        // フェードアウト指示の場合にはスルー
        //--------------------------------
        if (bActive == false)
        {
            return;
        }
    }

}

