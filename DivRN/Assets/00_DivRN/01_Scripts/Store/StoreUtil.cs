	//----------------------------------------------------------------------------
	/*!
		@brief	商品情報取得
	*/
	//----------------------------------------------------------------------------
	static	public StoreProduct GetProductFromNum( int nAccessNum )
	{
		if( StoreManager.Instance == null ){
			return null;
		}
		else{
			return StoreManager.Instance.GetProductFromNum( nAccessNum );
		}
	}
	//----------------------------------------------------------------------------
	/*!
		@brief	商品情報取得
	*/
	//----------------------------------------------------------------------------
	static	public StoreProduct GetProductFromID( string strProductID )
	{
		if( StoreManager.Instance == null ){
			return null;
		}
		else{
			return StoreManager.Instance.GetProductFromID( strProductID );
		}
	}
	//----------------------------------------------------------------------------
	/*!
		@brief	商品情報取得
	*/
	//----------------------------------------------------------------------------
	static	public StoreProduct GetReplaceEventProduct( StoreProduct srcProduct )
	{
		if( StoreManager.Instance == null ){
			return null;
		}
		else{
			return StoreManager.Instance.ReplaceEventProduct( srcProduct );
		}
	}
